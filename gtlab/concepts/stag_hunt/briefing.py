"""
Stag Hunt — onboarding briefing content.
"""

STORY = (
    "Two hunters can go after a stag together for a big shared meal, "
    "or each grab a hare alone — small, but guaranteed. "
    "The stag needs both of them to show up. "
    "The hare needs no one. "
    "Each decides without knowing what the other will do."
)

HOW_IT_WORKS = (
    "Each round has two steps. "
    "First, your opponent announces whether they plan to hunt Stag or Hare — "
    "but that announcement is just words, and some bots bluff. "
    "Then you both commit to an actual move. "
    "You see their announcement before you commit; they see yours too. "
    "Plain payoffs: if you both commit Stag, you each earn the best outcome — "
    "a real stag haul for both. "
    "If you commit Stag while they take the hare, you go home empty. "
    "If both commit Hare, you each get the small safe payoff. "
    "You'll face several bot personalities in turn. "
    "The bots also play a round-robin against each other in the background, "
    "so the standings show how every approach holds up — yours included."
)

WHAT_TO_WATCH = (
    "Pay attention to what the announcements are actually worth. "
    "Does trust build over rounds, or does it fall apart? "
    "Who keeps their word? Who bluffs? "
    "Notice which approaches survive the full arc of the arena "
    "— not just what works in a single round."
)

WHY_IT_MATTERS = (
    "This is the assurance problem: cooperation is not just best for both sides — "
    "it's also safe, provided you trust the other to actually show up. "
    "The whole game turns on that 'if.' "
    "You see this structure in teams that need everyone to contribute, "
    "in treaties that only hold if both sides honor them, "
    "in everyday 'I will if you will' situations. "
    "The announcements are a tool — cheap talk that might, or might not, "
    "mean anything."
)

YOUR_JOB = (
    "Each round: read the opponent's announcement, then commit Stag or Hare."
)
