"""
Ultimatum & Dictator — onboarding briefing content.
"""

STORY = (
    "One player is handed a prize and must decide how to split it with a second player. "
    "The second player can accept the offer — in which case both keep their shares — "
    "or reject it, and then both walk away with nothing at all. "
    "The proposer gets to decide the split; the responder gets to decide whether it happens."
)

HOW_IT_WORKS = (
    "You alternate roles round to round: sometimes you are the proposer, "
    "setting the split with a slider; sometimes you are the responder, "
    "choosing to Accept or Reject the offer you're shown. "
    "Some rounds are <strong>Dictator rounds</strong> — the responder has no veto "
    "and simply receives whatever the proposer decides to offer. "
    "A stake dial lets you change the size of the prize. "
    "Opponents remember how you've treated them across rounds and may adjust accordingly."
)

WHAT_TO_WATCH = (
    "Notice what it feels like when you are offered a small share of a large pot — "
    "and whether that feeling changes depending on how big the pot is. "
    "As a proposer in a Dictator round, pay attention to what you actually offer "
    "when the other side can't say no. "
    "As a responder, notice the pull toward rejecting an offer "
    "even when accepting would leave you better off than walking away with nothing."
)

WHY_IT_MATTERS = (
    "Fairness turns out to be a force that can override cold arithmetic. "
    "People routinely reject offers that would make them better off in pure token terms — "
    "and proposers who know this often offer more than strict logic would suggest. "
    "Cold logic has a specific prediction for both sides: the responder should accept any "
    "positive offer (something beats nothing), so the proposer should offer almost nothing. "
    "But real proposers offer far more than that — because they expect to be punished "
    "for unfairness, and they're usually right. "
    "The two halves of the logic travel together: once you see that responders punish, "
    "you see why proposers give. "
    "These patterns show up wherever resources get divided: negotiations, settlements, "
    "compensation decisions, and anywhere one side sets the terms and the other decides whether to accept."
)

YOUR_JOB = "As proposer: choose the split. As responder: Accept or Reject."
